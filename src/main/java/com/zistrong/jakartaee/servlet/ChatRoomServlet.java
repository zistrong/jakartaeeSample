package com.zistrong.jakartaee.servlet;

import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.io.IOException;

@WebServlet(name = "ChatRoomServlet", value = "/chatroomHome")
public class ChatRoomServlet extends HttpServlet {
    private Logger logger = LoggerFactory.getLogger(ChatRoomServlet.class);
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        doPost(request, response);
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        request.setAttribute("roomId", request.getSession().getId());
        request.getRequestDispatcher("view/chatroom.jsp").forward(request, response);
    }
}
